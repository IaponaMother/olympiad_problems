N = int(input())
R = 0
for i in range(N):
    m = list(map(int, input().split(' ')))
    S = 0.5 * (abs((m[2] - m[0]) * (m[5] - m[1]) - (m[4] - m[0])*(m[3] - m[1])) + abs((m[4] - m[0]) * (m[7] - m[1]) - (m[6] - m[0])*(m[5] - m[1])) + abs((m[6] - m[0]) * (m[9] - m[1]) - (m[8] - m[0])*(m[7] - m[1])) + abs((m[8] - m[0]) * (m[3] - m[1]) - (m[2] - m[0])*(m[9] - m[1])))
    s1 = (((m[4] - m[2]) ** 2 + (m[5] - m[3]) ** 2) * ((m[6] - m[4]) ** 2 + (m[7] - m[5]) ** 2)) ** 0.5
    if S == s1:
        R += 1
print(R)
